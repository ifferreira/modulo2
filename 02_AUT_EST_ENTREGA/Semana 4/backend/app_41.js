	sql = "INSERT INTO tb_pessoa (nome_pessoa, cargo) VALUES ('" + req.body.nome + "', '" + req.body.cargo + "')";
	console.log(sql);
	db.run(sql, [],  err => {
		if (err) {
		    throw err;
		}	
	});
